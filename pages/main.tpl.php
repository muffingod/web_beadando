<h1>Eddie</h1>
<h2>A tanulás élmény</h2>

<div>Légy részese te is az élménynek a kiterjesztett valóság segítségével.</div>

<h2>Diákoknak</h2>
<p>Te is unod a száraz tankönyveket? Nem vagy egyedül! Éld át a kiterjesztett valóság adta élményt,
és tudj meg többet a tananyagról, mint amit a könyvek adhatnak.</p>
<h2>Szülőknek</h2>
<p>Mit gondolsz, a telefon és az iskola lehetnek barátok? Az Eddie célja, hogy a diákok tanulási
folyamatát élményszerűbbé tegye a modern technológiák és a gyerekek mai ingerküszöbéhez méltó megoldásokkal.</p>